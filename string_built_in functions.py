name = "my name is hussain akbar  "

print(name.capitalize()) # first letter of the string becomes capital

print(name.title())  # first letter of every word in string becomes captal

print(name.split()) # break the string into small parts an return a list

print(name.isnumeric())  # check string is in numeric or not

print(name.strip())   # remove spaces 

print(name.rstrip()) # remove spaces from right side

print(name.lstrip()) # remove spaes from let side

print(name.upper())  # repace all the string in uppercase

print(name.lower()) # covert string in loower case

print(name.islower()) # check string is in lower case








